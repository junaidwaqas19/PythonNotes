# Example with variables having bad names
a = 10                  # Single-letter variable names can be unclear
var1 = "Example"        # Non-descriptive variable names
my_var_ = 20            # Underscore at the end is generally unnecessary
TotalStudents = 100     # Mixed-case variable names can be confusing (use snake_case instead)

# Printing variables with bad names
print("a:", a)
print("var1:", var1)
print("my_var_:", my_var_)
print("TotalStudents:", TotalStudents)
