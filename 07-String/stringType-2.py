# Newline - Inserts a newline character
string_with_newline = "Hello\nWorld"
print(string_with_newline)
# Output:
# Hello
# World

# Tab - Inserts a tab character
string_with_tab = "Hello\tWorld"
print(string_with_tab)  # Output: Hello    World

# Carriage Return - Moves the cursor to the beginning of the line
string_with_carriage_return = "Hello\rWorld"
print(string_with_carriage_return)  # Output: World

# Single Quote - Inserts a single quote character
string_with_single_quote = 'She said, \'Hello\''
print(string_with_single_quote)  # Output: She said, 'Hello'

# Double Quote - Inserts a double quote character
string_with_double_quote = "He said, \"Hello\""
print(string_with_double_quote)  # Output: He said, "Hello"

# Backslash - Inserts a backslash character
string_with_backslash = "This is a backslash: \\"
print(string_with_backslash)  # Output: This is a backslash: \
