matrix=[[1,2,3],
        [4,5,6],
        [7,8,9]]

# display 2nd row 2nd column value
print(matrix[1][1])

# display 3rd row first column value
print(matrix[2][0])

# nested_matrix
matrix_nested=[[1,2,3],
        [4,[4,5,8],6],
        [7,8,9]]
print(matrix_nested[1][1][0])
# multi nested
matrix_nested_nes=[[1,2,3],
        [4,[4,[5,9,1],8],6],
        [7,8,9]]
print(matrix_nested_nes[1][1][1][2])

